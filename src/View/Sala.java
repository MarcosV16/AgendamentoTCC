    private void jbntEditarSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbntEditarSalaActionPerformed
        // TODO add your handling code here:
        
        String codigo_sala = jtxtCodigoSala.getText();
        String nome_sala = jtxtNomeSala.getText();
        String bloco = jtxtBloco.getText();

        try (Connection conexao = ConexaoBD.obterConexao()) {
            String sql = "UPDATE sala SET nome_sala = ?, bloco = ? WHERE codigo_sala = ?";
            try (PreparedStatement preparedStatement = conexao.prepareStatement(sql)) {
                preparedStatement.setString(1, nome_sala);
                preparedStatement.setString(2, bloco);
                preparedStatement.setString(3, codigo_sala);

                int linhasAfetadas = preparedStatement.executeUpdate();

                if (linhasAfetadas > 0) {
                    exibirMensagemSucesso();
                } else {
                    exibirMensagemErro();
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            exibirMensagemErro();
        }   
        
    }//GEN-LAST:event_jbntEditarSalaActionPerformed

